local wk = require("which-key")

-- <leader> で開く “チートシート”的メニュー
wk.register({
  b = {
    name = "Basic",           -- 保存・終了など
    w = { ":w<CR>",  "保存 (write)" },
    q = { ":q<CR>",  "終了 (quit)" },
    x = { ":x<CR>",  "保存して終了" },
    e = { ":e<CR>",  "再読み込み/開く (edit)" },
  },
  m = {
    name = "Move",            -- 移動
    g = { "gg",  "ファイル先頭へ" },
    G = { "G",   "ファイル末尾へ" },
    ["0"] = { "0", "行頭へ" },
    ["$"] = { "$", "行末へ" },
    u = { "<C-u>", "半画面上へ" },
    d = { "<C-d>", "半画面下へ" },
  },
  e = {
    name = "Edit",            -- 編集
    x = { "x",    "1文字削除" },
    d = { "dd",   "1行削除" },
    y = { "yy",   "1行コピー" },
    p = { "p",    "貼り付け" },
    u = { "u",    "元に戻す (undo)" },
    r = { "<C-r>","やり直し (redo)" },
    [">"] = { ">>","インデント増" },
    ["<"] = { "<<","インデント減" },
  },
  s = {
    name = "Search",          -- 検索
    s = { "/<C-r><C-w><CR>", "カーソル下の単語を検索" },
    n = { "n",               "次を検索" },
    N = { "N",               "前を検索" },
    ["/"] = { "/",           "検索プロンプトを開く" },
  },
  w = {
    name = "Window/Tab",      -- 画面分割・タブ
    s = { ":split<CR>",   "水平分割" },
    v = { ":vsplit<CR>",  "垂直分割" },
    h = { "<C-w>h",       "左の分割へ" },
    j = { "<C-w>j",       "下の分割へ" },
    k = { "<C-w>k",       "上の分割へ" },
    l = { "<C-w>l",       "右の分割へ" },
    t = { ":tabnew<CR>",  "新しいタブ" },
    n = { "gt",           "次のタブ" },
    p = { "gT",           "前のタブ" },
  },
}, { prefix = "<leader>" })